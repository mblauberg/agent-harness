# Navigation/meta files that are never filing prose (don't lint them as such).
_NON_FILING = {"readme.md", "index.md"}


def iter_files(paths: list[Path], include_readme: bool) -> tuple[list[Path], int]:
    files: list[Path] = []
    failures = 0
    for path in paths:
        if path.is_dir():
            try:
                # Path.rglob suppresses some traversal errors on newer Python
                # versions. Walk explicitly so an unreadable root or child
                # directory cannot become a false-green empty scan.
                pending = [path]
                discovered: list[Path] = []
                while pending:
                    directory = pending.pop()
                    with os.scandir(directory) as entries:
                        rows = sorted(entries, key=lambda entry: entry.name)
                    child_dirs: list[Path] = []
                    for entry in rows:
                        if entry.is_dir(follow_symlinks=False):
                            child_dirs.append(Path(entry.path))
                        elif (
                            entry.is_file(follow_symlinks=False)
                            and entry.name.lower().endswith(".md")
                            and (include_readme or entry.name.lower() not in _NON_FILING)
                        ):
                            discovered.append(Path(entry.path))
                    pending.extend(reversed(child_dirs))
                files.extend(sorted(discovered))
            except OSError as exc:
                print(f"FAIL unreadable directory: {path}: {exc}", file=sys.stderr)
                failures += 1
        elif path.is_file():
            if include_readme or path.name.lower() not in _NON_FILING:
                files.append(path)
        else:
            print(f"FAIL missing path: {path}", file=sys.stderr)
            failures += 1
    return files, failures


def strip_markdown_code_spans(line: str) -> str:
    return re.sub(r"`[^`]*`", "", line)


def is_markdown_code_fence(line: str) -> bool:
    stripped = line.lstrip()
    return stripped.startswith("```") or stripped.startswith("~~~")


LONG_SENTENCE_WORDS = 60
LONG_SENTENCE_REPORT_CAP = 3  # worst offenders per file; the rest are counted, not printed
_SENTENCE_SPLIT = re.compile(r"(?<=[.!?])\s+(?=[A-Z(])")
_NON_PROSE_PREFIXES = ("|", "#", "```", "~~~", ">", "---")
_LIST_MARKER = re.compile(r"^(?:[-*+]\s|\d{1,3}[.)]\s|\(?[a-z]{1,4}[.)]\s|\([ivx]+\)\s)")


def check_long_sentences(path: Path, text: str) -> int:
    """WARN on prose sentences over LONG_SENTENCE_WORDS words (paragraphs reassembled across
    hard-wrapped lines; tables, headings, fences and blockquotes skipped; each list item is its
    own prose unit). Abbreviation false splits only shorten detected sentences, so this under-
    rather than over-fires. Only the LONG_SENTENCE_REPORT_CAP worst per file are printed."""
    hits: list[tuple[int, int, str]] = []  # (words, line_no, excerpt)
    para_lines: list[str] = []
    para_start = 0
    in_fence = False

    def flush() -> None:
        if not para_lines:
            return
        paragraph = strip_markdown_code_spans(" ".join(para_lines))
        for sentence in _SENTENCE_SPLIT.split(paragraph):
            words = len(sentence.split())
            if words > LONG_SENTENCE_WORDS:
                hits.append((words, para_start, sentence.strip()[:90]))

    for line_no, line in enumerate(text.splitlines(), start=1):
        stripped = line.strip()
        if is_markdown_code_fence(line):
            in_fence = not in_fence
            flush()
            para_lines = []
            continue
        if in_fence or not stripped or any(stripped.startswith(p) for p in _NON_PROSE_PREFIXES):
            flush()
            para_lines = []
            continue
        if _LIST_MARKER.match(stripped):
            flush()
            para_lines = []
        if not para_lines:
            para_start = line_no
        para_lines.append(stripped)
    flush()

    for words, line_no, excerpt in sorted(hits, reverse=True)[:LONG_SENTENCE_REPORT_CAP]:
        print(
            f"WARN {path}:{line_no}: long sentence ({words} words; split or tabulate "
            f"unless precision requires it): {excerpt}..."
        )
    remainder = len(hits) - LONG_SENTENCE_REPORT_CAP
    if remainder > 0:
        print(f"WARN {path}: {remainder} more sentence(s) over {LONG_SENTENCE_WORDS} words (not printed)")
    return min(len(hits), LONG_SENTENCE_REPORT_CAP) + (1 if remainder > 0 else 0)


# --- Internal leak tokens hidden in HTML comments ---------------------------------------------
# pandoc strips <!-- --> comments, so a leak token parked in one never renders and slips past a
# visual/PDF review, yet it still ships in the source markdown. Ban the leak TOKENS, never the
# comment mechanism: a plain "<!-- editor note -->" carrying none of these fires nothing.
_HTML_COMMENT = re.compile(r"<!--(.*?)-->", re.DOTALL)
_COMMENT_LEAK_TOKENS = [
    ("run-dir / audit path", re.compile(r"\bdocs/audits/")),
    ("scratch / build path", re.compile(r"(?<![A-Za-z.])\.work/|\breview_set/|/scratchpad|/private/tmp/")),
    ("absolute repo path", re.compile(r"/Users/[^\s`)>\]]+")),
    ("internal task id", re.compile(r"\bTASK-[A-Z]+-\d+\b")),
]


def check_html_comment_leaks(path: Path, text: str, allow_quoted_examples: bool = False) -> int:
    """FAIL on internal leak tokens hidden inside HTML comments: run-dir/audit paths, .work/
    scratchpad/build paths, absolute repo paths and TASK-<AREA>-<n> ids. Scope is the comment body
    only, so a plain "<!-- editor note -->" with no leak token never fires. Skipped under
    --allow-quoted-examples (the skill's own reference docs quote these tokens as examples)."""
    if allow_quoted_examples:
        return 0
    failures = 0
    for match in _HTML_COMMENT.finditer(text):
        body = match.group(1)
        line_no = text.count("\n", 0, match.start()) + 1
        for label, pattern in _COMMENT_LEAK_TOKENS:
            hit = pattern.search(body)
            if hit:
                print(f"FAIL {path}:{line_no}: internal leak token in HTML comment ({label}): {hit.group(0).strip()}")
                failures += 1
    return failures


# --- Headings glued to the previous line -------------------------------------------------------
# The build renders markdown via pandoc with blank_before_header: an ATX heading whose previous
# line is non-blank is NOT parsed as a heading and renders as body text, silently demoting a whole
# Part/section in the filed PDF (observed live on file 10 Parts D/H and file 51 Part C before the
# 2026-07-05 sweep). Code fences are skipped (# is a comment there).
_ATX_HEADING = re.compile(r"^ {0,3}#{1,6}\s")


def check_heading_blank_lines(path: Path, text: str) -> int:
    """FAIL on an ATX heading not preceded by a blank line (pandoc blank_before_header renders it
    as body text). First line of the file is exempt; fenced code blocks are skipped."""
    failures = 0
    lines = text.splitlines()
    in_fence = False
    for idx, line in enumerate(lines):
        if is_markdown_code_fence(line):
            in_fence = not in_fence
            continue
        if in_fence:
            continue
        if _ATX_HEADING.match(line) and idx > 0 and lines[idx - 1].strip():
            print(
                f"FAIL {path}:{idx + 1}: heading without preceding blank line "
                f"(renders as body text under blank_before_header): {line.strip()[:80]}"
            )
            failures += 1
    return failures


def check_file(path: Path, allow_quoted_examples: bool = False) -> tuple[int, int]:
    failures = 0
    warnings = 0
    try:
        text = path.read_text(encoding="utf-8")
    except UnicodeDecodeError:
        try:
            text = path.read_text(encoding="utf-8", errors="replace")
        except OSError as exc:
            print(f"FAIL {path}: unreadable input: {exc}", file=sys.stderr)
            return 1, 0
    except OSError as exc:
        print(f"FAIL {path}: unreadable input: {exc}", file=sys.stderr)
        return 1, 0

    in_code_fence = False
    for line_no, line in enumerate(text.splitlines(), start=1):
        if allow_quoted_examples and is_markdown_code_fence(line):
            in_code_fence = not in_code_fence
            continue
        if allow_quoted_examples and in_code_fence:
            continue
        if allow_quoted_examples and line.lstrip().startswith(">"):
            continue
        scan_line = strip_markdown_code_spans(line) if allow_quoted_examples else line
        for label, pattern in FAIL_PATTERNS:
            if pattern.search(scan_line):
                print(f"FAIL {path}:{line_no}: {label}: {line.strip()}")
                failures += 1
        for label, pattern in WARN_PATTERNS:
            if pattern.search(scan_line):
                print(f"WARN {path}:{line_no}: {label}: {line.strip()}")
                warnings += 1
    warnings += check_long_sentences(path, text)
    failures += check_html_comment_leaks(path, text, allow_quoted_examples)
    failures += check_heading_blank_lines(path, text)
    return failures, warnings


def main() -> int:
    parser = argparse.ArgumentParser(description="Lint filing-facing Australian legal markdown for style risks.")
    parser.add_argument(
        "paths",
        nargs="+",
        type=Path,
        help="One or more explicit project files or directories to scan (no project defaults).",
    )
    parser.add_argument("--include-readme", action="store_true", help="Include README.md files in scans.")
    parser.add_argument(
        "--allow-empty",
        action="store_true",
        help="Allow an existing input set to resolve to no Markdown files; missing or unreadable explicit paths still fail.",
    )
    parser.add_argument(
        "--allow-quoted-examples",
        action="store_true",
        help="Ignore text inside Markdown code spans; useful when checking skill references that quote banned phrases.",
    )
    args = parser.parse_args()

    total_failures = 0
    total_warnings = 0
    files, resolution_failures = iter_files(args.paths, args.include_readme)
    total_failures += resolution_failures
    if not files and not args.allow_empty:
        print("FAIL no Markdown files resolved from the explicit inputs.", file=sys.stderr)
        total_failures += 1
    for path in files:
        failures, warnings = check_file(path, args.allow_quoted_examples)
        total_failures += failures
        total_warnings += warnings

    print(f"Scanned {len(files)} markdown file(s): {total_failures} failure(s), {total_warnings} warning(s).")
    return 1 if total_failures else 0


if __name__ == "__main__":
    raise SystemExit(main())
